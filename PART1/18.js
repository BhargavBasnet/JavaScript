//Switch Statement

let day = 3;
switch (day)
{
    case 0:
        console.log("Sunday")
        break;
    case 1:
        console.log("Monday")
        break;
    case 2:
        console.log("Tuesday")
        break;
    case 3:
        console.log("Wednesday")
        break;
    case 4:
        console.log("thurday")
        break;
    case 5:
        console.log("Friday")
        break;
    default:
        console.log("invalid day")
}
